(* Definition of nested harmonic sums *)


HS[a_, jList__, (n_)?IntegerQ] := HS[a, jList, n] = Sum[((a/Abs[a])^i/i^Abs[a])*HS[jList, i], {i, 1, n}]; 
HS[a_, (n_)?IntegerQ] := HS[a, n] = Sum[(a/Abs[a])^i/i^Abs[a], {i, 1, n}]; 


(* Convert Euler-Zagier sums to usual nested harmonic sums *)


ZSToHS[X_] := Block[{subdiv}, 
   subdiv = ((Flatten[#1, 1] & )[((#1 //. {i___, 0, i2_ /; i2 != 0, i3___} :> {i, Sign[i2]*(Abs[i2] + 1), i3} //. 
             {i___, 0, i2_ /; i2 != 0} :> {i, Sign[i2]*(Abs[i2] + 1)} & ) /@ #1 & )[((DeleteCases[#1, {__, 0}] & ) /@ #1 & )[
          (Tuples[{0, 1}, #1] & ) /@ Range[Length[X], Length[X]]]]] & )[X]; 
    subdiv = ((Table[X[[1 + Sum[{##1}[[k]], {k, 1, i - 1}] ;; Sum[{##1}[[k]], {k, 1, i}]]], {i, 1, Length[{##1}]}] & )[
        Sequence @@ #1] & ) /@ subdiv; subdiv = ((Plus @@ Abs[#1]*Times @@ Sign[#1] & ) /@ #1 & ) /@ subdiv; 
    (Plus @@ (#1 /. S[0] -> 0) & )[((-1)^(1 + Length[#1])*(#1 /. {i__} :> S[i]) & ) /@ subdiv*(-1)^(1 + Length[subdiv[[-1]]])]]


(* Convert nested harmonic sums to combination of Psi-functions and MZV's *)


SToPsi[X_] := (Plus @@ #1 & )[((-1)^Length[X /. Subscript[S, d__] :> {d}]*#1 /. Subscript[\[Zeta], a__] :> 
        Subscript[\[Zeta], Sequence @@ Reverse[{a}]] & )[
     (#1 /. {a_, b___} :> (-1)^Length[{a, b}]*Subscript[\[Zeta], Sequence @@ a]*Sequence @@ (Subscript[\[Zeta], Sequence @@ #1] & ) /@ 
             {b} - (-1)^Length[{a, b}]*Subscript[S, Sequence @@ a]*Sequence @@ (Subscript[\[Zeta], Sequence @@ #1] & ) /@ 
             {b} & ) /@ ((Table[(X /. Subscript[S, d__] :> {d})[[1 + Sum[{##1}[[k]], {k, 1, i - 1}] ;; 
             Sum[{##1}[[k]], {k, 1, i}]]], {i, 1, Length[{##1}]}] & )[Sequence @@ #1] & ) /@ 
       (#1 //. {a___, 0, i_ /; i != 0, b___} :> {a, i + 1, b} & ) /@ (DeleteCases[#1, {___, 0}] & )[
         Tuples[{0, 1}, Length[X /. Subscript[S, d__] :> {d}]]]]]; 


(* Extract Li({1},{1}) from multiple polylogarithm *)


Li1Extract[X_] := FixedPoint[
    Expand[#1 /. Li[1, a__] :> ((-(Plus @@ #1 & )[((#1 /. {b__} :> Li[b] & ) /@ #1 & )[(((#1 + Abs[{a}])*Sign[{a}] & ) /@ 
                  #1 & )[Permutations[Join[Range[Length[{a}] - 1]*0, {1}]]]]] + Li1*Li[a] - 
            Sum[Li[Sequence @@ Insert[{a}, 1, i]], {i, 2, Length[{a}] + 1}])/If[Split[{a}][[1]][[1]] == 1, 
            Length[Split[{a}][[1]]] + 1, 1] /. Li[Sequence @@ Join[{1}, Split[{a}][[1]]], c___] :> 0) /. 
       Subscript[S, i__] :> (-1)^(Length[{i}] + 1)*Li[{i}]] & , X]; 


(* MZV's substitution *)


ZetaSubs[X_] := (Collect[#1, w] & )[
   Expand[
    X /. Subscript[\[Zeta], a__] :> Li[a] /. \[Zeta][a__] :> Li[a] /. Li[1, a__] :> Li1Extract[Li[1, a]] /. Li[a__] :> Re[Li[a]] + 
                I*Im[Li[a]] /. DLiSubsRe /. DLiSubsIm /. Re[Li[a__]] :> Re[Li[Sequence @@ Conjugate[{a}]]] /. 
          Im[Li[a__]] :> -Im[Li[Sequence @@ Conjugate[{a}]]] /. DLiSubsRe /. DLiSubsIm /. Zeta[3] -> z3 /. Zeta[5] -> z5 /. 
     Zeta[7] -> z7]]


(* Differentation of Z-sums, harmonic sums or MZV over w *)


SDiffW[X_, a1__, sign_, wExp_] := 
   (Plus @@ ({1, w, w^2/2, w^3/6, w^4/24, w^5/5!, w^6/6!, w^7/7!, w^8/8!, w^9/9!}[[1 ;; wExp + 1]]*
         FoldList[Expand[#1 /. S[i__] :> Expand[(Plus @@ #1 & )[((#1 /. {ii__, F[a_]} :> (-1)^sign*a*S[Sequence @@ ii] & ) /@ 
                   #1 & )[({(Abs[{i}] + #1)*Sign[{i}], F[Plus @@ Abs[{i}*#1]]} & ) /@ Permutations[PadLeft[{1}, 
                    Length[{i}]]]]]]] & , #1, {w, w^2, w^3, w^4, w^5, w^6, w^7, w^8, w^9}[[1 ;; wExp]]]) & ) /@ 
      (S[a1] + aa0*S[1]) /. aa0 -> 0 /. S[i__] :> X[i]; 


(* Analytic continuation of Psi-function *)


PsiAC[X_, (r_)?IntegerQ, (r0_)?NonNegative, ord:_?NonNegative:0] := Block[{RRR1, RRR2, RRR3, RRR4, a00}, 
    RRR1 = X + a00 /. S[a__] :> Subscript[S, a]; 
     RRR1 = Expand[RRR1 /. Subscript[\[Zeta], a__] :> Li[a] /. Li[1, a__] :> Li1Extract[Li[1, a]] /. Li[a__] :> ZetaSubs[Li[a]]]; 
     If[r < 0, RRR2 = ((CoefficientList[#1/w^Exponent[#1, w, Min], w, ord + 1 - Exponent[#1, w, Min]] . 
             Table[w^i, {i, Exponent[#1, w, Min], 0 + ord}] & )[(#1 /. ZS[i__] :> ZSToHS[{i}] & )[
            #1 /. Subscript[S, a__] :> (Times @@ Sign[{a}]^r0*(Sum[Subscript[ZS, Abs[{0, a}[[i]]], Sequence @@ Conjugate[
                              {0, a}[[1 ;; i - 1]]]]*(1/w^Abs[{0, a}[[i]]])*Subscript[\[Zeta], Abs[{0, a}[[i]]], Sequence @@ 
                             Reverse[{0, a}[[i + 1 ;; Length[{a}] + 1]]]], {i, 1, Length[{a}] + 1}] + Sum[Subscript[ZS, 
                            Abs[{0, a}[[i]]], Sequence @@ Conjugate[{0, a}[[1 ;; i]]]]*Subscript[\[Zeta], Abs[{0, a}[[i]]], 
                            Sequence @@ Reverse[{0, a}[[i + 1 ;; Length[{a}] + 1]]]], {i, 1, Length[{a}] + 1}]))/
                       Times @@ Sign[{1, a}[[1 ;; Length[{a}] + 1]]]^r /. Subscript[ZS, i_, 0, a__] :> Subscript[ZS, i, a] /. 
                   Subscript[\[Zeta]] -> 1 /. Subscript[\[Zeta], i_] :> 1 /. Subscript[ZS, i_, 0] -> 1 /. Subscript[ZS, 0] -> 0 /. 
               Subscript[ZS, k_, i__] :> (-1)^Plus @@ Abs[{i}]*Subscript[ZS, k, i] /. Subscript[\[Zeta], k_, a1__] :> SDiffW[\[Zeta], 
                a1, 1, k + 0 + ord] /. Subscript[ZS, k_, a1__] :> SDiffW[ZS, a1, 0, k + 0 + ord]]] & ) /@ RRR1; , 
      RRR2 = ((CoefficientList[#1/w^Exponent[#1, w, Min], w, ord + 1 - Exponent[#1, w, Min]] . 
            Table[w^i, {i, Exponent[#1, w, Min], 0 + ord}] & )[
          #1 /. Subscript[S, a__] :> (Times @@ Sign[{a}]^r0/Times @@ Sign[{1, a}[[1 ;; Length[{a}] + 1]]]^r)*
                    Sum[Subscript[S, Abs[{0, a}[[i]]], Sequence @@ {0, a}[[1 ;; i]]]*Subscript[\[Zeta], Abs[{0, a}[[i]]], 
                       Sequence @@ Reverse[{0, a}[[i + 1 ;; Length[{a}] + 1]]]], {i, 1, Length[{a}] + 1}] /. 
                 Subscript[S, i_, 0, a__] :> Subscript[S, i, a] /. Subscript[\[Zeta]] -> 1 /. Subscript[\[Zeta], i_] :> 1 /. 
              Subscript[S, i_, 0] -> 1 /. Subscript[S, k_, i__] :> (-1)^Length[{i}]*Subscript[S, k, i] /. 
            Subscript[\[Zeta], k_, a1__] :> SDiffW[\[Zeta], a1, 1, ord + k + 0] /. Subscript[S, k_, a1__] :> 
            SDiffW[S, a1, 1, ord + k + 0]] & ) /@ RRR1]; 
     RRR4 = ((Collect[#1, w] & )[Expand[#1 /. Subscript[\[Zeta], a__] :> Li[a] /. \[Zeta][a__] :> Li[a] /. 
            Li[1, a__] :> Li1Extract[Li[1, a]] /. Li[a__] :> ZetaSubs[Li[a]]]] & ) /@ RRR2; RRR4 /. a00 -> 0]; 


(* Analytic continuation of nested harmonic sum *)


HSAC[X_, (r_)?IntegerQ, (r0_)?NonNegative, ord:_?NonNegative:0] := Block[{RRR1, RRR2, RRR3, RRR4, a00}, 
    RRR1 = X + a00 /. S[a__] :> Subscript[S, a]; 
     RRR1 = (#1 /. S[i__] :> Subscript[S, i] /. Subscript[S, a__] :> SToPsi[#1] + a00 & )[X]; 
     RRR1 = Expand[RRR1 /. Subscript[\[Zeta], a__] :> Li[a] /. Li[1, a__] :> Li1Extract[Li[1, a]] /. Li[a__] :> ZetaSubs[Li[a]]]; 
     If[r < 0, RRR2 = ((CoefficientList[#1/w^Exponent[#1, w, Min], w, ord + 1 - Exponent[#1, w, Min]] . 
             Table[w^i, {i, Exponent[#1, w, Min], 0 + ord}] & )[(#1 /. ZS[i__] :> ZSToHS[{i}] & )[
            #1 /. Subscript[S, a__] :> Times @@ Sign[{a}]^r0*(Sum[(Subscript[ZS, Abs[{0, a}[[i]]], Sequence @@ Conjugate[
                              {0, a}[[1 ;; i - 1]]]]*(1/w^Abs[{0, a}[[i]]])*Subscript[\[Zeta], Abs[{0, a}[[i]]], Sequence @@ 
                             Reverse[{0, a}[[i + 1 ;; Length[{a}] + 1]]]])/Times @@ Sign[{1, a}[[1 ;; Length[{a}] + 1]]]^r, 
                         {i, 1, Length[{a}] + 1}] + Sum[(Subscript[ZS, Abs[{0, a}[[i]]], Sequence @@ Conjugate[{0, a}[[
                               1 ;; i]]]]*Subscript[\[Zeta], Abs[{0, a}[[i]]], Sequence @@ Reverse[{0, a}[[i + 1 ;; Length[{a}] + 
                                 1]]]])/Times @@ Sign[{1, a}[[1 ;; Length[{a}] + 1]]]^r, {i, 1, Length[{a}] + 1}]) /. 
                    Subscript[ZS, i_, 0, a__] :> Subscript[ZS, i, a] /. Subscript[\[Zeta]] -> 1 /. Subscript[\[Zeta], i_] :> 1 /. 
                 Subscript[ZS, i_, 0] -> 1 /. Subscript[ZS, 0] -> 0 /. Subscript[ZS, k_, i__] :> (-1)^Plus @@ Abs[{i}]*
                 Subscript[ZS, k, i] /. Subscript[\[Zeta], k_, a1__] :> SDiffW[\[Zeta], a1, 1, k + 0 + ord] /. 
             Subscript[ZS, k_, a1__] :> SDiffW[ZS, a1, 0, k + 0 + ord]]] & ) /@ RRR1; , 
      RRR2 = ((CoefficientList[#1/w^Exponent[#1, w, Min], w, ord + 1 - Exponent[#1, w, Min]] . 
            Table[w^i, {i, Exponent[#1, w, Min], 0 + ord}] & )[
          #1 /. Subscript[S, a__] :> Times @@ Sign[{a}]^r0*Sum[Conjugate[Times @@ Sign[{1, a}[[1 ;; Length[{a}] + 1]]]^r]*
                      Subscript[S, Abs[{0, a}[[i]]], Sequence @@ {0, a}[[1 ;; i]]]*Subscript[\[Zeta], Abs[{0, a}[[i]]], 
                       Sequence @@ Reverse[{0, a}[[i + 1 ;; Length[{a}] + 1]]]], {i, 1, Length[{a}] + 1}] /. 
                 Subscript[S, i_, 0, a__] :> Subscript[S, i, a] /. Subscript[\[Zeta]] -> 1 /. Subscript[\[Zeta], i_] :> 1 /. 
              Subscript[S, i_, 0] -> 1 /. Subscript[S, k_, i__] :> (-1)^Length[{i}]*Subscript[S, k, i] /. 
            Subscript[\[Zeta], k_, a1__] :> SDiffW[\[Zeta], a1, 1, ord + k + 0] /. Subscript[S, k_, a1__] :> 
            SDiffW[S, a1, 1, ord + k + 0]] & ) /@ RRR1]; 
     RRR4 = ((Collect[#1, w] & )[Expand[#1 /. Subscript[\[Zeta], a__] :> Li[a] /. \[Zeta][a__] :> Li[a] /. 
            Li[1, a__] :> Li1Extract[Li[1, a]] /. Li[a__] :> ZetaSubs[Li[a]]]] & ) /@ RRR2; RRR4 /. a00 -> 0]; 


